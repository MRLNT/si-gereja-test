								<div class="accordion_panel">
									<div>
										<p>Pada awal tahun 1967 jemaat yang berdomisili di Asrama BRIMOB - BOGOR sepakat untuk membuat satu persekutuan bernama “Parmingguon Cibuluh”. Pada waktu itu pelayanan masih dilakukan dari HKBP Bogor Jln. Paledang. Sebagai penanggungjawab Bapak St.R.B.Panggabean (Let.Kol).
<br>
Selama “Parmingguon Cibuluh” berjalan di wilayah Asrama BRIMOB, tiga kali jemaat berpindah tempat mengadakan kebaktian. Kemudian pada tahun 1969 “Parhalado” dan Jemaat sepakat untuk membeli sebidang tanah dengan ukuran 10 x 14 M, atas usaha dari Bapak R. Hutagaol.
<br>
Pada tahun 1970 jemaat semakin berkembang, sehingga perlu mengangkat Sintua untuk melayani jemaat lokal, waktu itu St.S.M.Siahaan, St.S.T.Hutagalung dan St. F. Lubis ditahbiskan menjadi sintua. Jumlah jemaat saat itu + 23 KK. HKBP Parmingguon Cibuluh waktu itu dipimpin oleh St.S.M.Siahaan.
<br>
Tahun 1971 direncanakan untuk membangun gedung gereja walaupun saat itu keadaan keuangan belum memadai. Tetapi karena semangat dan antusias jemaat pembangunan tetap berjalan sehingga berdiri satu bangunan gereja yang terbuat dari Bilik Bambu (Dingding Topas). Pada hari Minggu tanggal 31 Mei 1971 merupakan Kebaktian Pertama (Parmingguon Parjolo) di gereja yang baru dibangun tersebut, dan nama “Parmingguan Cibuluh” berobah menjadi Parmingguan HKBP Ciluar.
<br>
Pada tanggal 19 Oktober 1980, Parmingguan HKBP Ciluar diresmikan menjadi Gereja Penuh (Huria na Gok) bernama : Gereja HKBP Ciluar yang ber-resort ke HKBP Resort Bogor, Jln. Paledang No. 11 yang mana pada waktu itu dipimpin oleh Pdt. G.M. Tambun. Hari Minggu Tgl. 19 Oktober 1980 kemudian ditetapkan menjadi Hari Lahir Gereja HKBP Ciluar. Bapak St. S.T. Hutagalung terpilih sebagai Wakil Guru Huria (Vorgangeer) pertama dan Ketua Panitia waktu itu Bapak St. Ir.M.Silalahi. Pada tahun itu juga Luas Tanah (Pertapahan) gereja bertambah menjadi 367 M2.
<br>
Pada Tahun 1985 Bapak St. M. Sihombing terpilih sebagai Wakil Guru Huria menggantikan Bapak St. S.T.Hutagalung. Pendeta di Resort Bogor pada waktu itu adalah Pdt. Hutapea.
<br>
Tahun 1987 kedaan gedung Gereja sudah mulai rusak, dan direncanakan membangun atau merehab bangunan gereja yang lama. Luas Tanah (Pertapahan) bertambah menjadi 670 M2 sumbangan dari Bapak Haji Junaedi.
<br>
Pada Tahun 1992-1998 terjadi pergolakan di HKBP, HKBP Resort Bogor dimekarkan menjadi dua resort, yaitu HKBP Resort Bogor dan HKBP Resort Villa Duta – Bogor, dimana HKBP Ciluar kemudian menjadi salah satu “pagaran”nya.
<br>
Pada Tanggal 15 Oktober 1995 diadakan Pesta “Mameakhon Batu Ojahan” (MBO) dan sebagai Ketua Panitia terpilih waktu itu adalah Bapak: H.N.Simbolon. Pesta MBO langsung dihadiri Pimpinan HKBP: Sekjen Pdt. Dr. S.M.Siahaan dan Pdt.Dr.Bonar Napitupulu (Ephorus sekarang). Hadir juga pada acara tersebut : Bapak Jenderal Purn. Parapat (yang memberi sumbangan semua Keramik untuk bangunan gereja) dan Bapak Raja D.L.Sitorus (memberi sumbangan semua Bata Merah untuk bangunan gereja).
<br>
Pada tahun 1998, gereja HKBP Ciluar berencana untuk memperluas tanah. Rencana ini gagal karena PT Sinar Proses tidak menjual sebagian tanahnya, tetapi harus sekaligus. Tanah itu sekarang sudah menajadi milik Mall Dua Raja, yang kemudian berganti nama : Ciluar Plaza. Pembangunan Jembatan juga batal, karena ada rencana dari pihak Plaza untuk membangun jembatan yang permanent. Dana untuk Tanah dan Jembatan tersebut dialihkan untuk membangun Rumah Pendeta di lantai II.
<br>
Pada Bulan Mei 2005 Pimpinan Jemaat HKBP Ciluar diserah-terimakan dari St. M. Sihombing kepada Pdt. Zending Sinurat,STh (Pelayan Full Timer dari Pusat), yang pindah dari HKBP Petapahan Res. Lubuk Pakam – Dist.X Medan - Aceh
<br>
Pada tanggal 23 Oktober 2005, Gereja HKBP Ciluar mengadakan Pesta Jubileum 25 Tahun. Pesta ini dihadiri oleh Bapak Praeses HKBP Distrik VIII Jawa Kalimantan Pdt. Drs. B.D.F.Sidabutar,STh. Panitia Pesta Jubileum diketuai oleh : Bapak Ir. J.P.Hutapea. Sasaran utama dari Pesta adalah pengadaan AC Gereja (masih 8 unit), untuk mangantisipasi berdirinya Ciluar Plaza (pas bersebelahan dengan gereja) yang sedikit-banyaknya akan mempengaruhi kenyamanan beribadah di gereja HKBP Ciluar.
<br>
Sejak tgl.10 Pebruari 2008 Pimpinan Jemaat HKBP Ciluar sudah berganti. Pdt.Z.Sinurat,STH pindah tugas ke HKBP Salembaran Resort Tangerang Kota. Sedangkan yang menggantikan di HKBP Ciluar adalah Pdt.S.H.Silitonga,STh yang pindah dari HKBP Res. Brebes.</p>
